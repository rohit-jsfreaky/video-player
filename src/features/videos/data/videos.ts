// ─── Helper: generate a pseudo-random but stable duration from the slug ─────

function generateDuration(slug: string): number {
  let hash = 0;
  for (let i = 0; i < slug.length; i++) {
    hash = (hash << 5) - hash + slug.charCodeAt(i);
    hash |= 0;
  }
  // Returns a duration between 3:00 (180s) and 15:00 (900s)
  return 180 + Math.abs(hash % 720);
}

